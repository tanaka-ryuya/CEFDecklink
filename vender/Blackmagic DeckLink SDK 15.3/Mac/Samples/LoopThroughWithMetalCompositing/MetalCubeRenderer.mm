static const OSType kSupportedYUVPixelFormat = 'v210';
static const OSType kSupportedRGBPixelFormat = 'R10l';

simd_float3x3 getYCbCrToRGBMatrix(CFStringRef colorPrimaries)
{
	if (CFStringCompare(colorPrimaries, kCVImageBufferColorPrimaries_ITU_R_2020, 0) == kCFCompareEqualTo)
	{
		// Use ITU-R BT.2020 matrix
		return simd_matrix_from_rows(simd_make_float3( 1.0,  0.0   ,  1.4746 ),
									 simd_make_float3( 1.0, -0.1646, -0.5714 ),
									 simd_make_float3( 1.0,  1.8814,  0.0    ));
	}
	else if (CFStringCompare(colorPrimaries, kCVImageBufferColorPrimaries_SMPTE_C, 0) == kCFCompareEqualTo ||
		CFStringCompare(colorPrimaries, kCVImageBufferColorPrimaries_EBU_3213, 0) == kCFCompareEqualTo)
	{
		// Use ITU-R BT.601 matrix
		return simd_matrix_from_rows(simd_make_float3( 1.0,  0.0  ,  1.402 ),
									 simd_make_float3( 1.0, -0.344, -0.714 ),
									 simd_make_float3( 1.0,  1.772,  0.0   ));
	}
	else
	{
		// Use ITU-R BT.709 matrix for all other color primaries values
		return simd_matrix_from_rows(simd_make_float3( 1.0,  0.0   ,  1.5748 ),
									 simd_make_float3( 1.0, -0.1873, -0.4681 ),
									 simd_make_float3( 1.0,  1.8556,  0.0    ));
	}
}

simd_float3x3 getRGBToYCbCrMatrix(CFStringRef colorPrimaries)
{
	if (CFStringCompare(colorPrimaries, kCVImageBufferColorPrimaries_ITU_R_2020, 0) == kCFCompareEqualTo)
	{
		// Use ITU-R BT.2020 matrix
		return simd_matrix_from_rows(simd_make_float3(  0.2627,  0.6780,  0.0593 ),
									 simd_make_float3( -0.1396, -0.3604,  0.5000 ),
									 simd_make_float3(  0.5000, -0.4598, -0.0402 ));
	}
	else if (CFStringCompare(colorPrimaries, kCVImageBufferColorPrimaries_SMPTE_C, 0) == kCFCompareEqualTo ||
		CFStringCompare(colorPrimaries, kCVImageBufferColorPrimaries_EBU_3213, 0) == kCFCompareEqualTo)
	{
		// Use ITU-R BT.601 matrix
		return simd_matrix_from_rows(simd_make_float3(  0.299,  0.587,  0.114 ),
									 simd_make_float3( -0.169, -0.331,  0.500 ),
									 simd_make_float3(  0.500, -0.419, -0.081 ));
	}
	else
	{
		// Use ITU-R BT.709 matrix for all other color primaries values
		return simd_matrix_from_rows(simd_make_float3(  0.2126,  0.7152,  0.0722 ),
									 simd_make_float3( -0.1145, -0.3854,  0.5000 ),
									 simd_make_float3(  0.5000, -0.4542, -0.0458 ));
	}
}

simd_float4x4 getRotationMatrix(float angle, simd_float3 axis)
{
	float rad = M_PI * angle / 180.0;
	SCNMatrix4 matrix = SCNMatrix4MakeRotation(rad, axis.x, axis.y, axis.z);
	return SCNMatrix4ToMat4(matrix);
}

simd_float4x4 getScaleMatrix(simd_float3 scale)
{
	SCNMatrix4 matrix = SCNMatrix4MakeScale(scale.x, scale.y, scale.z);
	return SCNMatrix4ToMat4(matrix);
}

simd_float4x4 getTranslateMatrix(simd_float3 translate)
{
	SCNMatrix4 matrix = SCNMatrix4MakeTranslation(translate.x, translate.y, translate.z);
	return SCNMatrix4ToMat4(matrix);
}

simd_float4x4 getPerspectiveMatrix(float fovy, float aspect, float zNear, float zFar)
{
	float fovyRadians = M_PI * fovy / 180.0;
	float f = 1.0 / tan(fovyRadians/2);
	
	return simd_matrix_from_rows (
		simd_make_float4( f/aspect, 0.0,  0.0,                        0.0                       ),
		simd_make_float4( 0.0,      f,    0.0,                        0.0                       ),
		simd_make_float4( 0.0,      0.0,  (zFar+zNear)/(zFar-zNear),  2*zFar*zNear/(zNear-zFar) ),
		simd_make_float4( 0.0,      0.0,  -1.0,                       0.0                       )
	);
}

// Delegate so we can access renderer from C++
@class MetalRenderer;

struct MetalRendererDelegate
{
	MetalRenderer* renderer;
};

// The main class performing the rendering.
@interface MetalRenderer : NSObject
@end

@implementation MetalRenderer
{
	id<MTLDevice> _device;

	id<MTLCommandQueue> _commandQueue;

	id<MTLComputePipelineState> _inputConversionComputePipelineState;
	id<MTLComputePipelineState> _outputConversionComputePipelineState;
	id<MTLRenderPipelineState> _renderPipelineState;
	id<MTLDepthStencilState> _depthStencilState;

	// Input and output texture caches that map to CVPixelBuffer's
	CVMetalTextureCacheRef _srcTextureCache;
	CVMetalTextureCacheRef _dstTextureCache;
	
	// Texture object used by the compute pipeline to convert input
	id<MTLTexture> _conversionTexture;
	
	// Texture object for the render pipeline
	id<MTLTexture> _renderTexture;

	// Texture object for the depth stecil
	id<MTLTexture> _depthTexture;
	
	// Buffer to hold cube vertex data
	id<MTLBuffer> _cubeVertices;
	NSInteger _cubeVertexCount;
	
	// Input conversion compute kernel dispatch parameters
	MTLSize _computeEncoderThreadgroupSize;
	MTLSize _computeEncoderThreadgroupCount;
	
	OSType _pixelFormat;
	
	float _cubeRotationAngle;
	float _cubeRotationAngleRate;
}


- (id)initWithTextureSize:(NSSize)textureSize
			  pixelFormat:(OSType)pixelFormat
				frameRate:(NSInteger)frameRate
{
	self = [super init];
	if (self)
	{
		NSError *error = nil;
		
		_device = MTLCreateSystemDefaultDevice();
		
		_pixelFormat = pixelFormat;
		
		id<MTLLibrary> library = [_device newDefaultLibrary];

		// Load the input conversion function from library and create compute pipeline state for it
		NSDictionary* inputConversionKernelDict = @{@(kSupportedYUVPixelFormat).stringValue: @"InputConversions::v210ToRGBA16FloatKernel",
													@(kSupportedRGBPixelFormat).stringValue: @"InputConversions::R10lToRGBA16FloatKernel"};
		
		NSString* inputConversionKernel = [inputConversionKernelDict objectForKey:@(pixelFormat).stringValue];
		NSAssert(inputConversionKernel, @"Unsupported pixel format");

		id<MTLFunction>	inputConversionFunction = [library newFunctionWithName:inputConversionKernel];
		_inputConversionComputePipelineState = [_device newComputePipelineStateWithFunction:inputConversionFunction
																					  error:&error];
		NSAssert(_inputConversionComputePipelineState, @"Failed to create input conversion compute pipeline state: %@", error);

		// Load the input conversion function from library and create compute pipeline state for it
		NSDictionary* outputConversionKernelDict = @{@(kSupportedYUVPixelFormat).stringValue: @"OutputConversions::RGBA16FloatTov210Kernel",
													@(kSupportedRGBPixelFormat).stringValue: @"OutputConversions::RGBA16FloatToR10lKernel"};
		
		NSString* outputConversionKernel = [outputConversionKernelDict objectForKey:@(pixelFormat).stringValue];
		NSAssert(outputConversionKernel, @"Unsupported pixel format");

		id<MTLFunction>	outputConversionFunction = [library newFunctionWithName:outputConversionKernel];
		_outputConversionComputePipelineState = [_device newComputePipelineStateWithFunction:outputConversionFunction
																					   error:&error];
		NSAssert(_inputConversionComputePipelineState, @"Failed to create output conversion compute pipeline state: %@", error);

		// Setup texture that the Metal pipeline write unpacked and converted incoming frame
		MTLTextureDescriptor* inputConversionTextureDescription = [[MTLTextureDescriptor alloc] init];
		inputConversionTextureDescription.textureType = MTLTextureType2D;
		inputConversionTextureDescription.pixelFormat = MTLPixelFormatRGBA16Float;
		inputConversionTextureDescription.width = textureSize.width;
		inputConversionTextureDescription.height = textureSize.height;
		inputConversionTextureDescription.usage = MTLTextureUsageShaderWrite | MTLTextureUsageShaderRead;
		
		_conversionTexture = [_device newTextureWithDescriptor:inputConversionTextureDescription];
		NSAssert(_conversionTexture, @"Failed to create conversion texture");

		// Set the compute kernel's threadgroup size to 40 x 18 and count to cover entire image
		_computeEncoderThreadgroupSize = MTLSizeMake(40, 18, 1);
		_computeEncoderThreadgroupCount.width  = (textureSize.width + _computeEncoderThreadgroupSize.width -  1) / _computeEncoderThreadgroupSize.width;
		_computeEncoderThreadgroupCount.height = (textureSize.height + _computeEncoderThreadgroupSize.height - 1) / _computeEncoderThreadgroupSize.height;
		_computeEncoderThreadgroupCount.depth = 1;
		
		MTLPixelFormat depthTextureFormat = MTLPixelFormatDepth32Float;
		MTLTextureDescriptor *depthTextureDescriptor = [MTLTextureDescriptor texture2DDescriptorWithPixelFormat:depthTextureFormat width:textureSize.width height:textureSize.height mipmapped:NO];
		// Setup texture descriptor for depth buffer.
		// First check whether we can support memoryless render target
		// see: https://developer.apple.com/metal/Metal-Feature-Set-Tables.pdf
		BOOL memorylessSupported = [_device supportsFamily:MTLGPUFamilyApple2];
		
		depthTextureDescriptor.usage = MTLTextureUsageRenderTarget;
		depthTextureDescriptor.storageMode = memorylessSupported ? MTLStorageModeMemoryless : MTLStorageModePrivate;
		
		_depthTexture = [_device newTextureWithDescriptor:depthTextureDescriptor];
		_depthTexture.label = @"Depth Texture";

		// Setup texture descriptor for offscreen render buffer.
		MTLPixelFormat renderTextureFormat = MTLPixelFormatRGBA16Float;
		MTLTextureDescriptor *renderTextureDescriptor = [MTLTextureDescriptor texture2DDescriptorWithPixelFormat:renderTextureFormat width:textureSize.width height:textureSize.height mipmapped:NO];
		renderTextureDescriptor.storageMode = MTLStorageModePrivate;
		renderTextureDescriptor.usage = MTLTextureUsageRenderTarget | MTLTextureUsageShaderRead;

		_renderTexture = [_device newTextureWithDescriptor:renderTextureDescriptor];
		_renderTexture.label = @"Render Texture";

		// Load vertex and fragment shaders from metal file
		id<MTLFunction> vertexFunction = [library newFunctionWithName:@"vertexShader"];
		id<MTLFunction> fragmentFunction = [library newFunctionWithName:@"fragmentShader"];

		// Create render pipeline descriptor
		MTLRenderPipelineDescriptor* pipelineDescriptor = [[MTLRenderPipelineDescriptor alloc] init];
		pipelineDescriptor.label = @"Offscreen Metal Render Pipeline";
		pipelineDescriptor.sampleCount = 1;
		pipelineDescriptor.vertexFunction = vertexFunction;
		pipelineDescriptor.fragmentFunction = fragmentFunction;
		pipelineDescriptor.colorAttachments[0].pixelFormat = MTLPixelFormatRGBA16Float;
		pipelineDescriptor.depthAttachmentPixelFormat = depthTextureFormat;
		pipelineDescriptor.stencilAttachmentPixelFormat = MTLPixelFormatInvalid;
		pipelineDescriptor.vertexBuffers[VertexShaderInputIndexVertices].mutability = MTLMutabilityImmutable;

		_renderPipelineState = [_device newRenderPipelineStateWithDescriptor:pipelineDescriptor error:&error];
		NSAssert(_renderPipelineState, @"Failed to create render pipeline state: %@", error);

		// Create depth stencil descriptor
		MTLDepthStencilDescriptor* depthStencilDescriptor = [[MTLDepthStencilDescriptor alloc] init];
		depthStencilDescriptor.depthCompareFunction = MTLCompareFunctionLess;
		depthStencilDescriptor.depthWriteEnabled = YES;
		_depthStencilState = [_device newDepthStencilStateWithDescriptor:depthStencilDescriptor];

		// Create the command queue.
		_commandQueue = [_device newCommandQueue];
		_commandQueue.label = @"Metal Compositing Command Queue";
		
		// Create texture cache for input
		NSDictionary<NSString *, id> *cacheAttributes = @{
			(__bridge NSString *)kCVMetalTextureCacheMaximumTextureAgeKey: @0.5,
		};
		
		NSDictionary<NSString *, id> *srcTextureAttributes = @{
			(__bridge NSString *)kCVMetalTextureUsage: @(MTLTextureUsageShaderRead),
		};
		
		CVReturn status = CVMetalTextureCacheCreate(
			NULL,
			(__bridge CFDictionaryRef)cacheAttributes,
			_device,
			(__bridge CFDictionaryRef)srcTextureAttributes,
			&_srcTextureCache
		);
		NSAssert(_srcTextureCache != NULL, @"Failed to create texture cache: %d", status);

		NSDictionary<NSString *, id> *dstTextureAttributes = @{
			(__bridge NSString *)kCVMetalTextureUsage: @(MTLTextureUsageShaderWrite),
		};

		// Create texture cache for output
		status = CVMetalTextureCacheCreate(
			NULL,
			(__bridge CFDictionaryRef)cacheAttributes,
			_device,
			(__bridge CFDictionaryRef)dstTextureAttributes,
			&_dstTextureCache
		);
		NSAssert(_dstTextureCache != NULL, @"Failed to create texture cache: %d", status);
		
		_cubeVertices = [_device newBufferWithBytes:&Cube::getVertices()[0]
											 length:Cube::getByteSize()
											options:MTLResourceStorageModeShared];
		NSAssert(_cubeVertices != NULL, @"Failed to create buffer for cubde vertices");
		_cubeVertexCount = Cube::getVertexCount();
		
		_cubeRotationAngle = 0.0;
		_cubeRotationAngleRate = 35.0 / frameRate;
	}

	return self;
}

- (void)compositeFromPixelBuffer:(CVPixelBufferRef)srcPixelBuffer
				   toPixelBuffer:(CVPixelBufferRef)dstPixelBuffer
		   withCompletionHandler:(nullable RenderCompletedBlock)completionBlock
{
	_cubeRotationAngle -= _cubeRotationAngleRate;
	
	// Create new command buffer for each rendering pass
	id<MTLCommandBuffer> commandBuffer = [_commandQueue commandBuffer];
	commandBuffer.label = @"MetalLoopThroughCommandBuffer";
	
	size_t width = CVPixelBufferGetWidth(srcPixelBuffer);
	size_t height = CVPixelBufferGetHeight(srcPixelBuffer);
	
	// Get the color primaries from the source pixel buffer, will use these for conversions
	CFDictionaryRef srcPixelBufferAttachments = CVBufferCopyAttachments(srcPixelBuffer, kCVAttachmentMode_ShouldPropagate);
	CFStringRef colorPrimaries = (CFStringRef)CFDictionaryGetValue(srcPixelBufferAttachments, kCVImageBufferColorPrimariesKey);
	simd_float3x3 inputConversionMatrix = getYCbCrToRGBMatrix(colorPrimaries);
	
	// Get the cached texture associated with the source CVPixelBuffer
	CVMetalTextureRef srcTextureRef = NULL;
	OSType srcPixelFormat = CVPixelBufferGetPixelFormatType(srcPixelBuffer);
	CVReturn status = kCVReturnUnsupported;
	if (srcPixelFormat == kSupportedYUVPixelFormat)
	{
		// Create texture using RGBA32 format representing 6 pixels to pass to compute shader
		status = CVMetalTextureCacheCreateTextureFromImage(kCFAllocatorDefault,
														   _srcTextureCache,
														   srcPixelBuffer,
														   NULL,
														   MTLPixelFormatRGBA32Uint,
														   (width + 5) / 6,
														   height,
														   0,
														   &srcTextureRef);
	}
	else if (srcPixelFormat == kSupportedRGBPixelFormat)
	{
		// Create texture using R32 format representing 1 pixel to compute shader
		status = CVMetalTextureCacheCreateTextureFromImage(kCFAllocatorDefault,
														   _srcTextureCache,
														   srcPixelBuffer,
														   NULL,
														   MTLPixelFormatR32Uint,
														   width,
														   height,
														   0,
														   &srcTextureRef);

	}
	else
	{
		NSAssert(NO, @"Unsupported source pixel format: %d", srcPixelFormat);
	}
	NSAssert(srcTextureRef != NULL, @"Failed to create source texture %d", status);
	
	// Setup compute encoder to perform conversion from DeckLink pixel format to RGBA16
	id<MTLComputeCommandEncoder> inputComputeEncoder = [commandBuffer computeCommandEncoder];
	[inputComputeEncoder setComputePipelineState:_inputConversionComputePipelineState];
	[inputComputeEncoder setTexture:CVMetalTextureGetTexture(srcTextureRef) atIndex:InputConversionTextureIndexInput];
	[inputComputeEncoder setTexture:_conversionTexture atIndex:InputConversionTextureIndexOutput];
	[inputComputeEncoder setBytes:&inputConversionMatrix length:sizeof(simd_float3x3) atIndex:InputConversionBufferMatrixIndex];
	[inputComputeEncoder dispatchThreadgroups:_computeEncoderThreadgroupCount threadsPerThreadgroup:_computeEncoderThreadgroupSize];
	[inputComputeEncoder endEncoding];
	
	// Define offscreen render pass
	MTLRenderPassDescriptor* renderPassDescriptor = [MTLRenderPassDescriptor new];
	renderPassDescriptor.colorAttachments[0].texture = _renderTexture;
	renderPassDescriptor.colorAttachments[0].loadAction = MTLLoadActionClear;
	renderPassDescriptor.colorAttachments[0].clearColor = MTLClearColorMake(0.0, 0.0, 0.0, 1.0);
	renderPassDescriptor.colorAttachments[0].storeAction = MTLStoreActionStore;
	
	// Define depth buffer
	renderPassDescriptor.depthAttachment.texture = _depthTexture;
	renderPassDescriptor.depthAttachment.loadAction = MTLLoadActionClear;
	renderPassDescriptor.depthAttachment.clearDepth = 1.0;
	renderPassDescriptor.depthAttachment.storeAction = MTLStoreActionDontCare;
	
	// Create the render command encoder
	id<MTLRenderCommandEncoder> renderEncoder = [commandBuffer renderCommandEncoderWithDescriptor:renderPassDescriptor];
	renderEncoder.label = @"MetalCompositingEncoder";
	
	// Set the render command encoder state
	[renderEncoder setRenderPipelineState:_renderPipelineState];
	
	// Set depth stancil and cull mode
	[renderEncoder setDepthStencilState:_depthStencilState];
	[renderEncoder setFrontFacingWinding:MTLWindingCounterClockwise];
	[renderEncoder setCullMode:MTLCullModeBack];
	
	// Set rotation, translation, scale and perspective transformations for cube
	Uniforms shaderUniforms;
	shaderUniforms.rotationMatrix = getRotationMatrix(_cubeRotationAngle, simd_make_float3(1.0, 1.0, 1.0));
	shaderUniforms.translationMatrix = getTranslateMatrix(simd_make_float3(0.0, 0.0, 3.0));
	shaderUniforms.scaleMatrix = getScaleMatrix(simd_make_float3(0.25, 0.25, 0.25));
	shaderUniforms.perspectiveMatrix = getPerspectiveMatrix(45.0f, width / (float)height, 0.1f, 100.0f);

	[renderEncoder setVertexBytes:&shaderUniforms
						   length:sizeof(Uniforms)
						  atIndex:VertexShaderInputIndexUniforms];
	
	// Encode the vertex data
	[renderEncoder setVertexBuffer:_cubeVertices
							offset:0
						   atIndex:VertexShaderInputIndexVertices];

	// Encode the vertex input texture
	[renderEncoder setFragmentTexture:_conversionTexture
							  atIndex:0];
	
	// Draw the triangle vertices.
	[renderEncoder drawPrimitives:MTLPrimitiveTypeTriangle
					  vertexStart:0
					  vertexCount:_cubeVertexCount];
	
	// Finalize encoding.
	[renderEncoder endEncoding];
	
	// Use the sample color primaries from the source pixel buffer, will use for output conversion
	simd_float3x3 outputConversionMatrix = getRGBToYCbCrMatrix(colorPrimaries);

	// Get the cached texture associated with the destination CVPixelBuffer
	CVMetalTextureRef dstTextureRef = NULL;
	OSType dstPixelFormat = CVPixelBufferGetPixelFormatType(dstPixelBuffer);
	status = kCVReturnUnsupported;
	if (dstPixelFormat == kSupportedYUVPixelFormat)
	{
		// Create texture using RGBA32 format representing 6 pixels as output from compute shader
		status = CVMetalTextureCacheCreateTextureFromImage(kCFAllocatorDefault,
														   _dstTextureCache,
														   dstPixelBuffer,
														   NULL,
														   MTLPixelFormatRGBA32Uint,
														   (width + 5) / 6,
														   height,
														   0,
														   &dstTextureRef);
	}
	else if (dstPixelFormat == kSupportedRGBPixelFormat)
	{
		// Create texture using R32 format representing 1 pixel as output from compute shader
		status = CVMetalTextureCacheCreateTextureFromImage(kCFAllocatorDefault,
														   _dstTextureCache,
														   dstPixelBuffer,
														   NULL,
														   MTLPixelFormatR32Uint,
														   width,
														   height,
														   0,
														   &dstTextureRef);

	}
	else
	{
		NSAssert(NO, @"Unsupported destination pixel format: %d", dstPixelFormat);
	}
	NSAssert(dstTextureRef != NULL, @"Failed to create desination texture %d", status);

	// Setup compute encoder to perform conversion from RGBA16 to DeckLink pixel format
	id<MTLComputeCommandEncoder> outputComputeEncoder = [commandBuffer computeCommandEncoder];
	[outputComputeEncoder setComputePipelineState:_outputConversionComputePipelineState];
	[outputComputeEncoder setTexture:_renderTexture atIndex:OutputConversionTextureIndexInput];
	[outputComputeEncoder setTexture:CVMetalTextureGetTexture(dstTextureRef) atIndex:OutputConversionTextureIndexOutput];
	[outputComputeEncoder setBytes:&outputConversionMatrix length:sizeof(simd_float3x3) atIndex:OutputConversionBufferMatrixIndex];
	[outputComputeEncoder dispatchThreadgroups:_computeEncoderThreadgroupCount threadsPerThreadgroup:_computeEncoderThreadgroupSize];
	[outputComputeEncoder endEncoding];

	// Setup completion handler to signal to draw task that Metal has completed with frame
	__block RenderCompletedBlock completedHandlerCompletionBlock = completionBlock;
	[commandBuffer addCompletedHandler:^(id<MTLCommandBuffer> buffer) {
		if (completedHandlerCompletionBlock)
			completedHandlerCompletionBlock();
		
		CFRelease(srcTextureRef);
		CFRelease(dstTextureRef);
	}];
	
	// Finalize CPU work and submit the command buffer to the GPU.
	[commandBuffer commit];
}

@end

// Owner class

MetalCubeRenderer::MetalCubeRenderer()
{
	m_delegate = std::unique_ptr<MetalRendererDelegate>(new MetalRendererDelegate);
}

MetalCubeRenderer::~MetalCubeRenderer() = default;

bool MetalCubeRenderer::init(com_ptr<IDeckLinkDisplayMode>& displayMode, BMDPixelFormat pixelFormat)
{
	CGSize textureSize = { (double)displayMode->GetWidth(), (double)displayMode->GetHeight() };
	
	BMDTimeValue frameDuration;
	BMDTimeScale frameTimescale;
	if (displayMode->GetFrameRate(&frameDuration, &frameTimescale) != S_OK)
	{
		std::cerr << "Could not get output frame rate." << std::endl;
		return false;
	}
	
	m_delegate->renderer = [[MetalRenderer alloc] initWithTextureSize:textureSize
														  pixelFormat:(OSType)pixelFormat
															frameRate:frameTimescale / frameDuration];
	
	return true;
}


bool MetalCubeRenderer::composite(com_ptr<IDeckLinkVideoFrame>& srcVideoFrame, com_ptr<IDeckLinkVideoFrame>& dstVideoFrame, const RenderCompletedCallback& completionCallback)
{
	// DeckLinkAPI's default allocator natively creates an IOSurface, which we can pass to Metal's
	// texture cache from the CVPixelBuffer
	com_ptr<IDeckLinkMacVideoBuffer> inputVideoBuffer(IID_IDeckLinkMacVideoBuffer, srcVideoFrame);
	if (! inputVideoBuffer)
	{
		std::cerr << "Failed to get IDeckLinkMacVideoBuffer from input video frame." << std::endl;
		return false;
	}
	
	CVPixelBufferRef inputCVPixelBuffer;
	if (inputVideoBuffer->CreateCVPixelBufferRef((void**)&inputCVPixelBuffer) != S_OK)
	{
		std::cerr << "Failed to create CVPixelBufferRef from input video buffer." << std::endl;
		return false;
	}

	com_ptr<IDeckLinkMacVideoBuffer> outputVideoBuffer(IID_IDeckLinkMacVideoBuffer, dstVideoFrame);
	if (! outputVideoBuffer)
	{
		std::cerr << "Failed to get IDeckLinkMacVideoBuffer from output video frame." << std::endl;
		return false;
	}

	CVPixelBufferRef outputCVPixelBuffer;
	if (outputVideoBuffer->CreateCVPixelBufferRef((void**)&outputCVPixelBuffer) != S_OK)
	{
		std::cerr << "Failed to create CVPixelBufferRef from output video buffer." << std::endl;
		return false;
	}

	__block RenderCompletedCallback completionCallbackBlock = completionCallback;
	__block com_ptr<IDeckLinkVideoFrame> dstVideoFrameBlock = dstVideoFrame;
	[m_delegate->renderer compositeFromPixelBuffer:inputCVPixelBuffer
									 toPixelBuffer:outputCVPixelBuffer
							 withCompletionHandler:^{
		// Render completed, scheduled output frame
		completionCallbackBlock(dstVideoFrameBlock);
	}];
	
	// compositeFromPixelBuffer holds onto reference of CVPixelBufferRef objects, so we can release
	CVBufferRelease(inputCVPixelBuffer);
	CVBufferRelease(outputCVPixelBuffer);

	return true;
}

bool MetalCubeRenderer::getInputBufferAllocators(IDeckLinkVideoBufferAllocatorProvider** allocatorProvider)
{
	// Set the allocatorProvider output parameter as null and return false to use the default allocator for
	// input. The default input allocator natively creates IOSurface, which can be mapped to MTLTextures.
	*allocatorProvider = nullptr;
	return false;
}

bool MetalCubeRenderer::getOutputBuffer(uint32_t bufferSize, IDeckLinkVideoBuffer** buffer)
{
	// Set the buffer output parameter as null and return false to use default video buffers when video
	// frames are created. The default buffers are backed by IOSurface, which we can map to MTLTextures.
	*buffer = nullptr;
	return false;
}
