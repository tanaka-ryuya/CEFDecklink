#include <iostream>
#include <CoreVideo/CVPixelBuffer.h>
#include "LoopThroughWithCompositing.h"
#include "MetalCubeRenderer.h"

// Default display mode for products that do not support video input mode detection
constexpr BMDDisplayMode kDefaultDisplayMode = bmdModeHD1080p50;
constexpr BMDPixelFormat kDefaultPixelFormat = bmdFormat10BitYUV;

constexpr unsigned kOutputPrerollSize = 3;

inline std::string getFourCharCodeForPixelFormat(BMDPixelFormat pixelFormat)
{
	char fourChar[5] = {
		static_cast<char>((pixelFormat >> 24) & 0xFF),
		static_cast<char>((pixelFormat >> 16) & 0xFF),
		static_cast<char>((pixelFormat >> 8) & 0xFF),
		static_cast<char>(pixelFormat & 0xFF),
		0
	};
	return std::string(fourChar);
}


LoopThroughWithCompositing::LoopThroughWithCompositing() :
	m_captureDevice(nullptr),
	m_playoutDevice(nullptr)
{
}

bool LoopThroughWithCompositing::init()
{
	if (! initDeckLinkDevices())
		return false;
	
	if (! m_captureDevice->enable(kDefaultDisplayMode, kDefaultPixelFormat))
	{
		std::cerr << "Unable to enable input, is the device already in use?" << std::endl;
		return false;
	}
	
	// Wait for the video input signal to lock
	if (! m_captureDevice->waitForVideoInputSignalLocked())
	{
		std::cerr << "Timeout waiting for video input signal to lock." << std::endl;
		return false;
	}
	
	m_pixelFormat = m_captureDevice->isDetectedFormatRGB444() ? bmdFormat10BitRGBXLE : bmdFormat10BitYUV;
	m_displayMode = m_captureDevice->getDetectedDisplayMode();
	if (m_displayMode == bmdModeUnknown)
	{
		// Input mode detection is not supported by the target input device, use the default mode
		m_displayMode = kDefaultDisplayMode;
	}
	
	com_ptr<IDeckLinkDisplayMode> deckLinkDisplayMode;
	if (m_playoutDevice->getDeckLinkOutput()->GetDisplayMode(m_displayMode, deckLinkDisplayMode.releaseAndGetAddressOf()) != S_OK)
	{
		std::cerr << "Could not get output display mode." << std::endl;
		return false;
	}
	
	CFStringRef displayModeString;
	if (deckLinkDisplayMode->GetName(&displayModeString) != S_OK)
	{
		std::cerr << "Could not get display mode name." << std::endl;
		return false;
	}

	std::cout << "Using display mode: " << CFStringGetCStringPtr(displayModeString, kCFStringEncodingUTF8) << std::endl;
	CFRelease(displayModeString);

	std::cout << "Using pixel format: \'" << getFourCharCodeForPixelFormat(m_pixelFormat) << "\'" << std::endl;
	
	m_renderer = std::make_unique<MetalCubeRenderer>();
	m_renderer->init(deckLinkDisplayMode, m_pixelFormat);
	
	return true;
}

bool LoopThroughWithCompositing::start()
{
	// Set handler for compositing captured video frame
	m_captureDevice->onVideoFrameArrived([this] (com_ptr<IDeckLinkVideoInputFrame>& inputVideoFrame) {
		compositeVideoFrame(inputVideoFrame);
	});
	
	// Enable DeckLink output
	if (! m_playoutDevice->enable(m_displayMode))
	{
		std::cerr << "Could not enable video output." << std::endl;
		return false;
	}

	// Enable DeckLink input with detected mode
	if (! m_captureDevice->enable(m_displayMode, m_pixelFormat))
	{
		std::cerr << "Could not re-enable input." << std::endl;
		return false;
	}
	
	if (! m_captureDevice->start())
	{
		std::cerr << "Could not start streams." << std::endl;
		return false;
	}

	return true;
}

void LoopThroughWithCompositing::stop()
{
	m_captureDevice->disable();
	m_playoutDevice->disable();
}

void LoopThroughWithCompositing::compositeVideoFrame(com_ptr<IDeckLinkVideoInputFrame> inputVideoFrame)
{
	// Check that the input frame has valid source, otherwise it will have black video buffer
	if (inputVideoFrame->GetFlags() & bmdFrameHasNoInputSource)
		return;

	// Get the colorspace of the input frame
	com_ptr<IDeckLinkVideoFrameMetadataExtensions> inputVideoFrameMetadata(IID_IDeckLinkVideoFrameMetadataExtensions, inputVideoFrame);
	int64_t colorspace;
	if (inputVideoFrameMetadata->GetInt(bmdDeckLinkFrameMetadataColorspace, &colorspace) != S_OK)
		return;
	
	// Create an IDeckLinkMutableVideoFrame with a buffer natively backed for rendering
	com_ptr<IDeckLinkVideoBuffer> outputVideoBuffer;  // Can be null if using native allocation
	m_renderer->getOutputBuffer(0, outputVideoBuffer.releaseAndGetAddressOf());
	
	com_ptr<IDeckLinkMutableVideoFrame> outputVideoFrame;
	if (! m_playoutDevice->createVideoFrame(outputVideoFrame, m_pixelFormat, (BMDColorspace)colorspace, outputVideoBuffer))
		return;
	
	// Get the stream time relative to the output timescale
	BMDTimeValue frameStreamTime;
	if (inputVideoFrame->GetStreamTime(&frameStreamTime, nullptr, m_playoutDevice->getFrameTimescale()) != S_OK)
	{
		std::cerr << "Failed to get stream time from the video input frame." << std::endl;
		return;
	}
	
	com_ptr<IDeckLinkVideoFrame> srcVideoFrame(IID_IDeckLinkVideoFrame, inputVideoFrame);
	com_ptr<IDeckLinkVideoFrame> dstVideoFrame(IID_IDeckLinkVideoFrame, outputVideoFrame);

	m_renderer->composite(srcVideoFrame, dstVideoFrame, [&, frameStreamTime] (com_ptr<IDeckLinkVideoFrame> outputFrame) {
		// Render completed, scheduled output frame
		m_playoutDevice->scheduleVideoFrame(outputFrame, frameStreamTime);
	});
}

bool LoopThroughWithCompositing::initDeckLinkDevices()
{
	// Obtain DeckLink iterator object
	com_ptr<IDeckLinkIterator> deckLinkIterator = com_ptr<IDeckLinkIterator>(CreateDeckLinkIteratorInstance());
	if (! deckLinkIterator)
	{
		std::cerr << "This application requires the Desktop Video drivers installed." << std::endl;
		return false;
	}
	
	while (true)
	{
		// Iterate the next IDeckLink device
		com_ptr<IDeckLink> deckLink;
		if (deckLinkIterator->Next(deckLink.releaseAndGetAddressOf()) != S_OK)
		{
			std::cerr << "No further Desktop Video devices were found." << std::endl;
			return false;
		}
		
		// Query the attributes interface for the DeckLink device
		com_ptr<IDeckLinkProfileAttributes> deckLinkAttributes(IID_IDeckLinkProfileAttributes, deckLink);
		if (! deckLinkAttributes)
		{
			std::cerr << "Could not obtain the IDeckLinkAttributes interface." << std::endl;
			return false;
		}
		
		// Check whether the DeckLink device is an active state
		int64_t duplexMode;
		if (deckLinkAttributes->GetInt(BMDDeckLinkDuplex, &duplexMode) != S_OK)
		{
			std::cerr << "Could not obtain the duplex mode attribute for the DeckLink device." << std::endl;
			return false;
		}
		
		// Check that the DeckLink device is active, if not skip this device
		if ((BMDDuplexMode)duplexMode == bmdDuplexInactive)
			continue;
		
		// Check the IO support for device
		int64_t videoIOSupport;
		if (deckLinkAttributes->GetInt(BMDDeckLinkVideoIOSupport, &videoIOSupport) != S_OK)
		{
			std::cerr << "Could not obtain the IO support attribute for the DeckLink device." << std::endl;
			return false;
		}
		
		if (! m_captureDevice && ((BMDVideoIOSupport)videoIOSupport & bmdDeviceSupportsCapture))
		{
			try
			{
				m_captureDevice = std::make_unique<DeckLinkCapture>(deckLink);
			}
			catch (const std::exception& e)
			{
				std::cerr << e.what() << std::endl;
				return false;
			}
			
			std::cout << "Using input device: " << m_captureDevice->getDeviceName() << std::endl;
			
			// If input device is half duplex, skip output discovery
			if (! m_playoutDevice && ((BMDDuplexMode)duplexMode == bmdDuplexHalf))
				continue;
		}
		
		if (! m_playoutDevice && ((BMDVideoIOSupport)videoIOSupport & bmdDeviceSupportsPlayback))
		{
			try
			{
				m_playoutDevice = std::make_unique<DeckLinkPlayout>(deckLink, kOutputPrerollSize);
			}
			catch (const std::exception& e)
			{
				std::cerr << e.what() << std::endl;
				return false;
			}
			
			std::cout << "Using output device: " << m_playoutDevice->getDeviceName() << std::endl;
		}
		
		if (m_captureDevice && m_playoutDevice)
			break;
	}
	
	if (! m_captureDevice || ! m_playoutDevice)
	{
		std::cerr << "Could not find an input and output device" << std::endl;
		return false;
	}
	
	return true;
}
