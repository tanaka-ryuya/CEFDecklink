#include <atomic>
#include <chrono>
#include <iostream>

#include "DeckLinkCapture.h"

constexpr std::chrono::seconds kVideoInputLockTimeout{5};

class DeckLinkCapture::CallbackDelegate : public IDeckLinkInputCallback, public IDeckLinkNotificationCallback
{
public:
	CallbackDelegate(DeckLinkCapture* owner);

	// IDeckLinkInputCallback interface
	HRESULT	VideoInputFormatChanged(BMDVideoInputFormatChangedEvents notificationEvents, IDeckLinkDisplayMode *newDisplayMode, BMDDetectedVideoInputFormatFlags detectedSignalFlags) override;
	HRESULT	VideoInputFrameArrived(IDeckLinkVideoInputFrame* videoFrame, IDeckLinkAudioInputPacket* audioPacket) override;
	
	// IDeckLinkNotificationCallback interface
	HRESULT Notify(BMDNotifications topic, uint64_t param1, uint64_t param2) override;
	
	// IUnknown interface
	HRESULT	QueryInterface(REFIID iid, LPVOID *ppv) override;
	ULONG	AddRef() override;
	ULONG	Release() override;

protected:
	~CallbackDelegate() = default;
	
private:
	DeckLinkCapture*	m_owner;
	std::atomic<ULONG>	m_refCount;
};

/// DeckLinkInputCallback implementation

DeckLinkCapture::CallbackDelegate::CallbackDelegate(DeckLinkCapture* owner) :
	m_owner(owner),
	m_refCount(1)
{
}

HRESULT DeckLinkCapture::CallbackDelegate::QueryInterface(REFIID iid, LPVOID *ppv)
{
	HRESULT result = S_OK;
	
	if (ppv == nullptr)
		return E_POINTER;
	
	// Obtain the IUnknown interface and compare it the provided REFIID
	CFUUIDBytes iunknown = CFUUIDGetUUIDBytes(IUnknownUUID);
	if (memcmp(&iid, &iunknown, sizeof(REFIID)) == 0)
	{
		*ppv = this;
		AddRef();
	}
	else if (memcmp(&iid, &IID_IDeckLinkInputCallback, sizeof(REFIID)) == 0)
	{
		*ppv = static_cast<IDeckLinkInputCallback*>(this);
		AddRef();
	}
	else if (memcmp(&iid, &IID_IDeckLinkNotificationCallback, sizeof(REFIID)) == 0)
	{
		*ppv = static_cast<IDeckLinkNotificationCallback*>(this);
		AddRef();
	}
	else
	{
		*ppv = nullptr;
		result = E_NOINTERFACE;
	}
	
	return result;
}

ULONG DeckLinkCapture::CallbackDelegate::AddRef(void)
{
	return ++m_refCount;
}

ULONG DeckLinkCapture::CallbackDelegate::Release(void)
{
	ULONG newRefValue = --m_refCount;
	if (newRefValue == 0)
		delete this;
	
	return newRefValue;
}

HRESULT DeckLinkCapture::CallbackDelegate::VideoInputFormatChanged(BMDVideoInputFormatChangedEvents, IDeckLinkDisplayMode*, BMDDetectedVideoInputFormatFlags)
{
	// This sample does not support auto-format detection
	return S_OK;
}

HRESULT DeckLinkCapture::CallbackDelegate::VideoInputFrameArrived(IDeckLinkVideoInputFrame* videoFrame, IDeckLinkAudioInputPacket* audioPacket)
{
	m_owner->receivedVideoInputFrame(com_ptr<IDeckLinkVideoInputFrame>(videoFrame));
	return S_OK;
}

HRESULT DeckLinkCapture::CallbackDelegate::Notify(BMDNotifications topic, uint64_t param1, uint64_t param2)
{
	if (topic != bmdStatusChanged)
		return S_OK;
	
	if ((BMDDeckLinkStatusID)param1 != bmdDeckLinkStatusVideoInputSignalLocked)
		return S_OK;
	
	m_owner->notifyVideoInputLockedChanged();
	return S_OK;
}

/// DeckLinkDevice implementation

DeckLinkCapture::DeckLinkCapture(com_ptr<IDeckLink>& deckLink) :
	m_deckLink(deckLink),
	m_deckLinkInput(IID_IDeckLinkInput, deckLink),
	m_isCapturing(false),
	m_videoInputIsLocked(false)
{
	if (!m_deckLinkInput)
		throw new std::runtime_error("DeckLink device does not have an input interface.");
	
	m_callbackDelegate = make_com_ptr<CallbackDelegate>(this);
	
	// Set capture callback
	m_deckLinkInput->SetCallback(m_callbackDelegate.get());
}

DeckLinkCapture::~DeckLinkCapture()
{
	if (m_isCapturing)
		disable();
	m_deckLinkInput->SetCallback(nullptr);
}

std::string DeckLinkCapture::getDeviceName()
{
	CFStringRef displayName;
	if (m_deckLink->GetDisplayName(&displayName) != S_OK)
		return "DeckLink";

	std::string returnString(CFStringGetCStringPtr(displayName, kCFStringEncodingUTF8));
	CFRelease(displayName);
	return returnString;
}

bool DeckLinkCapture::waitForVideoInputSignalLocked()
{
	std::unique_lock<std::mutex> lock(m_mutex);
	com_ptr<IDeckLinkNotification> deckLinkNotification(IID_IDeckLinkNotification, m_deckLink);
	if (deckLinkNotification->Subscribe(bmdStatusChanged, m_callbackDelegate.get()) != S_OK)
		return false;
	
	m_videoInputIsLocked = isVideoInputSignalLocked();
	if (m_videoInputIsLocked)
	{
		deckLinkNotification->Unsubscribe(bmdStatusChanged, m_callbackDelegate.get());
		return true;
	}
	
	bool videoInputLocked = m_videoInputLockCondition.wait_for(lock, kVideoInputLockTimeout, [&] { return m_videoInputIsLocked; });
	
	deckLinkNotification->Unsubscribe(bmdStatusChanged, m_callbackDelegate.get());
	return videoInputLocked;
}

bool DeckLinkCapture::isDetectedFormatRGB444()
{
	com_ptr<IDeckLinkStatus> deckLinkStatus(IID_IDeckLinkStatus, m_deckLink);
	
	int64_t detectedVideoInputFormatFlags;
	if (deckLinkStatus->GetInt(bmdDeckLinkStatusDetectedVideoInputFormatFlags, &detectedVideoInputFormatFlags) != S_OK)
		return false;
	
	return (detectedVideoInputFormatFlags & bmdDetectedVideoInputRGB444) != 0;
}

BMDDisplayMode DeckLinkCapture::getDetectedDisplayMode()
{
	com_ptr<IDeckLinkStatus> deckLinkStatus(IID_IDeckLinkStatus, m_deckLink);
	
	int64_t detectedVideoInputMode;
	if (deckLinkStatus->GetInt(bmdDeckLinkStatusDetectedVideoInputMode, &detectedVideoInputMode) != S_OK)
		return bmdModeUnknown;
	
	return (BMDDisplayMode)detectedVideoInputMode;
}

void DeckLinkCapture::onVideoFrameArrived(const VideoFrameArrivedHandler handler)
{
	m_videoFrameArrivedHandler = handler;
}

bool DeckLinkCapture::enable(const BMDDisplayMode displayMode, const BMDPixelFormat pixelFormat)
{
	BMDVideoInputFlags videoInputFlags = bmdVideoInputFlagDefault;
	
	// Enable the video input with requested video mode and format
	if (m_deckLinkInput->EnableVideoInput(displayMode, pixelFormat, videoInputFlags) != S_OK)
		return false;
	
	return true;
}

bool DeckLinkCapture::start()
{
	// Start the capture
	if (m_deckLinkInput->StartStreams() != S_OK)
		return false;
	
	m_isCapturing = true;
	
	return true;
}

void DeckLinkCapture::disable()
{
	// Stop the capture and disable input
	m_deckLinkInput->StopStreams();
	m_deckLinkInput->DisableVideoInput();
	
	m_isCapturing = false;
}

void DeckLinkCapture::receivedVideoInputFrame(com_ptr<IDeckLinkVideoInputFrame> videoFrame)
{
	if (m_videoFrameArrivedHandler)
		m_videoFrameArrivedHandler(videoFrame);
}

void DeckLinkCapture::notifyVideoInputLockedChanged()
{
	{
		std::lock_guard<std::mutex> lock(m_mutex);
		m_videoInputIsLocked = isVideoInputSignalLocked();
	}
	m_videoInputLockCondition.notify_one();
}

bool DeckLinkCapture::isVideoInputSignalLocked()
{
	com_ptr<IDeckLinkStatus> deckLinkStatus(IID_IDeckLinkStatus, m_deckLink);

	bool inputVideoLocked = false;
	if (deckLinkStatus->GetFlag(bmdDeckLinkStatusVideoInputSignalLocked, &inputVideoLocked) != S_OK)
		return false;

	return inputVideoLocked;
}
