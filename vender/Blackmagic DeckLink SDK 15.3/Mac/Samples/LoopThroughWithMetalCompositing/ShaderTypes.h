#pragma once

#include <simd/simd.h>

typedef enum InputConversionTextureIndex
{
	InputConversionTextureIndexInput  = 0,
	InputConversionTextureIndexOutput = 1,
} InputConversionTextureIndex;

typedef enum InputConversionBufferIndex
{
	InputConversionBufferMatrixIndex  = 0,
} InputConversionBufferIndex;

typedef enum OutputConversionTextureIndex
{
	OutputConversionTextureIndexInput  = 0,
	OutputConversionTextureIndexOutput = 1,
} OutputConversionTextureIndex;

typedef enum OutputConversionBufferIndex
{
	OutputConversionBufferMatrixIndex  = 0,
} OutputConversionBufferIndex;

typedef enum VertexShaderInputIndex
{
	VertexShaderInputIndexVertices     = 0,
	VertexShaderInputIndexUniforms     = 1,
} VertexShaderInputIndex;

// Defines the layout of the vertex array to pass to shader
typedef struct
{
	vector_float3 position;
	vector_float2 textureCoordinate;
} Vertex;

typedef struct
{
	matrix_float4x4 perspectiveMatrix;
	matrix_float4x4 rotationMatrix;
	matrix_float4x4 translationMatrix;
	matrix_float4x4 scaleMatrix;
} Uniforms;

// Define YCbCr struct with can be used with any Metal data type
template <typename T>
struct YCbCr
{
	T Y;
	T Cb;
	T Cr;
};
