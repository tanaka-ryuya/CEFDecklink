#include "stdafx.h"
#include "mfapi.h"
#include "VideoSampleQueue.h"
#include "DeckLinkMediaBuffer.h"
#include "SampleQueue.h"

VideoSampleQueue::VideoSampleQueue()
	: m_refCount(1), 
	m_cancelCapture(false),
	m_videoInterlaced(FALSE)
{
}

// IUnknown methods

HRESULT VideoSampleQueue::QueryInterface(REFIID iid, LPVOID* ppv)
{
	*ppv = nullptr;
	return E_NOTIMPL;
}

ULONG VideoSampleQueue::AddRef(void)
{
	return ++m_refCount;
}

ULONG VideoSampleQueue::Release(void)
{
	ULONG newRefValue = --m_refCount;
	if (newRefValue == 0)
		delete this;
	return newRefValue;
}

bool VideoSampleQueue::WaitForInputSample(IMFSample** sample, bool& waitCancelled)
{
	CComPtr<IMFSample>					videoSample;
	CComPtr<IDeckLinkVideoInputFrame>	videoFrame;
	CComPtr<DeckLinkMediaBuffer>		videoBuffer;
	CComPtr<IMFMediaBuffer>				mediaBuffer;
	BMDTimeValue						frameTime;
	BMDTimeValue						frameDuration;
	HRESULT								hr = S_OK;

	{
		std::unique_lock<std::mutex> lock(m_videoFrameQueueMutex);
		m_videoFrameQueueCondition.wait(lock, [&]{ return !m_videoFrameQueue.empty() || m_cancelCapture; });

		if (!m_videoFrameQueue.empty())
		{
			videoFrame = m_videoFrameQueue.front();
			m_videoFrameQueue.pop();
		}	
	}

	if (videoFrame != nullptr)
	{
		// Get time and duration of captured frame
		hr = videoFrame->GetStreamTime(&frameTime, &frameDuration, kMFTimescale);
		if (hr != S_OK)
			goto bail;

		hr = videoFrame->QueryInterface(IID_DeckLinkMediaBuffer, (void**)&videoBuffer);
		if (hr != S_OK)
			goto bail;

		mediaBuffer = videoBuffer->getBuffer();

		// Create a media sample and add the buffer to the sample.
		hr = MFCreateSample(&videoSample);
		if (hr != S_OK)
			goto bail;

		hr = videoSample->AddBuffer(mediaBuffer);
		if (hr != S_OK)
			goto bail;

		// Set the time stamp and the duration.
		hr = videoSample->SetSampleTime((LONGLONG)frameTime);
		if (hr != S_OK)
			goto bail;
		
		hr = videoSample->SetSampleDuration((LONGLONG)frameDuration);
		if (hr != S_OK)
			goto bail;

		hr = videoSample->SetUINT32(MFSampleExtension_Interlaced, m_videoInterlaced);
		if (hr != S_OK)
			goto bail;

		*sample = videoSample.Detach();
	}

bail:
	waitCancelled = m_cancelCapture;

	return (hr == S_OK);
}

void VideoSampleQueue::VideoFrameArrived(CComPtr<IDeckLinkVideoInputFrame> frame)
{
	{
		std::lock_guard<std::mutex> lock(m_videoFrameQueueMutex);
		m_videoFrameQueue.push(frame);
	}
	m_videoFrameQueueCondition.notify_one();
}

void VideoSampleQueue::CancelCapture()
{
	{
		// signal cancel flag to terminate wait condition
		std::lock_guard<std::mutex> lock(m_videoFrameQueueMutex);
		m_cancelCapture = true;
	}
	m_videoFrameQueueCondition.notify_one();
}

void VideoSampleQueue::Reset()
{
	std::lock_guard<std::mutex> lock(m_videoFrameQueueMutex);
	m_cancelCapture = false;
}

void VideoSampleQueue::SetVideoInterlacedMode(const BOOL videoInterlaced)
{
	m_videoInterlaced = videoInterlaced;
}
