#include <cstdlib>
#include <cstring>
#include <stdexcept>

#include "VideoFrame3D.h"
#include "com_ptr.h"

VideoFrame3D::Provider::Provider(IDeckLinkMutableVideoFrame* parentFrame, com_ptr<IDeckLinkVideoFrame>& rightFrame) :
	m_refCount(1),
	m_parentFrame(parentFrame),
	m_rightFrame(rightFrame)
{
}

HRESULT VideoFrame3D::Provider::QueryInterface(REFIID iid, LPVOID* ppv)
{
	com_ptr<VideoFrame3D> videoFrame3D = make_com_ptr<VideoFrame3D>(m_parentFrame, m_rightFrame);
	return videoFrame3D->QueryInterface(iid, ppv);
}

ULONG VideoFrame3D::Provider::AddRef()
{
	return ++m_refCount;
}

ULONG VideoFrame3D::Provider::Release()
{
	ULONG refs = --m_refCount;
	if (!refs)
		delete this;
	return refs;
}

static inline bool CompareREFIID(const REFIID& ref1, const REFIID& ref2)
{
	return memcmp(&ref1, &ref2, sizeof(REFIID)) == 0;
}

// IUnknown methods
HRESULT VideoFrame3D::QueryInterface(REFIID iid, LPVOID *ppv)
{
	if (CompareREFIID(iid, IID_IUnknown))
		*ppv = static_cast<IUnknown*>(this);
	else if (CompareREFIID(iid, IID_IDeckLinkVideoFrame3DExtensions))
		*ppv = static_cast<IDeckLinkVideoFrame3DExtensions*>(this);
	else
		return m_owner->QueryInterface(iid, ppv);

	AddRef();
	return S_OK;
}

ULONG VideoFrame3D::AddRef(void)
{
	return ++m_refCount;
}

ULONG VideoFrame3D::Release(void)
{
	ULONG newRefValue = --m_refCount;
	if (!newRefValue)
		delete this;
	return newRefValue;
}

BMDVideo3DPackingFormat VideoFrame3D::Get3DPackingFormat(void)
{
	return bmdVideo3DPackingLeftOnly;
}

HRESULT VideoFrame3D::GetFrameForRightEye(IDeckLinkVideoFrame** rightEyeFrame)
{
	return m_frameRight->QueryInterface(IID_IDeckLinkVideoFrame, (void**)rightEyeFrame);
}

VideoFrame3D::VideoFrame3D(IDeckLinkMutableVideoFrame* owner, com_ptr<IDeckLinkVideoFrame>& right) :
	m_owner(owner),
	m_frameRight(right),
	m_refCount(1)
{
	if (!owner || !right)
		throw std::invalid_argument("frame null");
}

